function _leapMonth(y){return LUNAR_INFO[y-1900]&0xf;}
function _leapDays(y){if(_leapMonth(y))return(LUNAR_INFO[y-1900]&0x10000)?30:29;return 0;}
function _monthDays(y,m){return(LUNAR_INFO[y-1900]&(0x10000>>m))?30:29;}
function _lYearDays(y){let s=348;for(let i=0x8000;i>0x8;i>>=1)s+=(LUNAR_INFO[y-1900]&i)?1:0;return s+_leapDays(y);}

function lunar2solar(ly,lm,ld,isLeap){
  try{
    let offset=0;
    for(let i=1900;i<ly;i++) offset+=_lYearDays(i);
    const leapM=_leapMonth(ly);
    for(let i=1;i<lm;i++){
      offset+=_monthDays(ly,i);
      if(leapM===i) offset+=_leapDays(ly);
    }
    if(isLeap) offset+=_monthDays(ly,lm);
    offset+=ld-1;
    const base=new Date(Date.UTC(1900,0,31));
    const sol=new Date(base.getTime()+offset*86400000);
    return{sYear:sol.getUTCFullYear(),sMonth:sol.getUTCMonth()+1,sDay:sol.getUTCDate()};
  }catch(e){return null;}
}

// ── 사주 계산 함수 (index.html 그대로 추출) ─────────────────
function getYP(y,m,d){
  let yr=y;
  if(m<2||(m===2&&d<4)) yr=y-1;
  return{g:((yr-4)%10+10)%10, j:((yr-4)%12+12)%12};
}

function getMP(y,m,d,yg){
  let sm=m;
  if(d<절기입절[m-1]) sm=m-1;
  if(sm<=0) sm=12;
  const ji=(sm%12);
  const s=[2,4,6,8,0][yg%5];
  const ord=sm-2<0?sm+10:sm-2;
  return{g:(s+ord)%10, j:ji};
}

function getDP(y,m,d){
  const diff=Math.floor((new Date(y,m-1,d)-new Date(1900,0,1))/86400000);
  return{g:((0+diff)%10+10)%10, j:((10+diff)%12+12)%12};
}

function getHP(dg,h){
  let ji;
  if(h>=23||h<1) ji=0;
  else if(h<3)  ji=1;
  else if(h<5)  ji=2;
  else if(h<7)  ji=3;
  else if(h<9)  ji=4;
  else if(h<11) ji=5;
  else if(h<13) ji=6;
  else if(h<15) ji=7;
  else if(h<17) ji=8;
  else if(h<19) ji=9;
  else if(h<21) ji=10;
  else          ji=11;
  return{g:([0,2,4,6,8][dg%5]+ji)%10, j:ji};
}

// ── 헬퍼 ────────────────────────────────────────────────────
function pillarStr(p){ return `${천간[p.g]}${지지[p.j]}(${천간한자[p.g]}${지지한자[p.j]})`; }

function calcAll(y, m, d, h, label){
  const yp = getYP(y,m,d);
  const mp = getMP(y,m,d, yp.g);
  const dp = getDP(y,m,d);
  const hp = getHP(dp.g, h);
  console.log(`\n[${label}] → 양력 ${y}년 ${m}월 ${d}일 ${h}시`);
  console.log(`  년주: ${pillarStr(yp)}`);
  console.log(`  월주: ${pillarStr(mp)}`);
  console.log(`  일주: ${pillarStr(dp)}`);
  console.log(`  시주: ${pillarStr(hp)}`);
  return {yp, mp, dp, hp};
}

// ── 야자시(夜子時) 분리 버전: 23:xx → 다음날 일주 사용 ─────
function calcAllYaJasi(y, m, d, h, label){
  let dy=y, dm=m, dd=d;
  const isYaJasi = h>=23;
  if(isYaJasi){
    // 다음날로 이동
    const next = new Date(y, m-1, d+1);
    dy = next.getFullYear(); dm = next.getMonth()+1; dd = next.getDate();
  }
  const yp = getYP(dy,dm,dd);
  const mp = getMP(dy,dm,dd, yp.g);
  const dp = getDP(dy,dm,dd);
  const hp = getHP(dp.g, h);
  console.log(`  [야자시분리 적용] 다음날 기준: ${dy}/${dm}/${dd}`);
  console.log(`  년주: ${pillarStr(yp)}  월주: ${pillarStr(mp)}  일주: ${pillarStr(dp)}  시주: ${pillarStr(hp)}`);
  return {yp, mp, dp, hp};
}

// ── 입춘 시각 정밀 비교용 (년주만) ─────────────────────────
function getYP_exact(y, m, d, h, min, ipchunY, ipchunM, ipchunD, ipchunH, ipchunMin){
  // 입춘 시각 이전이면 yr-1
  const birthMin = y*525960 + m*43800 + d*1440 + h*60 + min;
  const ipMin    = ipchunY*525960 + ipchunM*43800 + ipchunD*1440 + ipchunH*60 + ipchunMin;
  let yr = (birthMin < ipMin) ? y-1 : y;
  return{g:((yr-4)%10+10)%10, j:((yr-4)%12+12)%12};
}

